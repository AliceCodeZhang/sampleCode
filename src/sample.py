class Caculator:
     def __init__(self, xx, yy):
         self.x = xx
         self.y = yy
    #This function adds two numbers
     def add(self):
         return self.x + self.y
     #This function subtracts two numbers
     def substrac(self):
         return self.x - self.y
     #This function multiplies two numbers
     def multiply(self):
         return self.x*self.y
     #This function divide two numbers
     def divide(self):
         return self.x/self.y

